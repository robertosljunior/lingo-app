// semantic-assessment-bridge.js — Slice V2.14. The explicit, versioned bridge
// between an ActivityPlanV2 and the language-analysis public API. It decides,
// from AUTHORED structured metadata only, WHICH semantic strategy an activity
// may use — and builds a complete SemanticAssessmentRequestV2 that carries every
// field the engine actually consumes (no field is ever silently dropped).
//
// Hard rules (Slice V2.14 §2, §4, §19):
//   - the strategy comes from the plan's authored `semantic_assessment`
//     metadata, never from the recipe name or from a text≠model mismatch;
//   - `requested_intent` is NEVER invented — it must be an authored value in the
//     real engine intent vocabulary, else the bridge falls back to `free`;
//   - `essential_words` are NEVER generated by heuristic — they are authored,
//     explicit, and non-empty for equivalent_meaning, else fallback to `free`;
//   - `context` is preserved for diagnostics ONLY; it never influences a verdict
//     (the engine does not consume a generic context parameter — §4);
//   - absence of metadata ⇒ strategy `free` (backward compatible, §30).
//
// PURE: no storage, no clock, no engine import.

export const SEMANTIC_ASSESSMENT_BRIDGE_VERSION = 1

export const SEMANTIC_STRATEGIES = ['free', 'guided_intent', 'equivalent_meaning']
export const SEMANTIC_ASSESSMENT_MODES = ['free', 'guided', 'equivalent']

// Real engine intent vocabulary (§3/§7): every `intent` declared by the
// knowledge-pack semantic_frames plus the structural-NLP intent_signals. A
// `guided_intent` metadata whose requested_intent is not in this set can never
// be satisfied by the engine, so it is rejected (validator) / falls back to
// free (runtime). Derived from frozen content; stable.
export const KNOWN_SEMANTIC_INTENTS = Object.freeze([
  'ability', 'action', 'description', 'future_plan', 'location', 'obligation',
  'past_experience', 'polite_request', 'possession', 'question', 'request', 'time',
])

export const FREE_FALLBACK_REASONS = [
  'NO_AUTHORED_SEMANTIC_TARGET',
  'INVALID_SEMANTIC_METADATA',
  'NON_PRODUCTION_RECIPE',
]

const PRODUCTION_RECIPES = new Set(['guided_production', 'free_production'])

function strategyToMode(strategy) {
  if (strategy === 'equivalent_meaning') return 'equivalent'
  if (strategy === 'guided_intent') return 'guided'
  return 'free'
}

/**
 * Validate an authored `semantic_assessment` metadata object against a
 * reference sentence. PURE. Returns { valid, errors: [code], normalized }.
 * Used by the content validator (§25) and, defensively, by the bridge.
 *   errors: INVALID_SEMANTIC_ASSESSMENT_STRATEGY, EQUIVALENT_TARGET_WITHOUT_TEXT,
 *   EQUIVALENT_TARGET_WITHOUT_ESSENTIAL_WORDS, SEMANTIC_TARGET_REFERENCES_NON_AUTHORED_TEXT,
 *   INVALID_REQUESTED_INTENT
 */
export function validateSemanticAssessmentMetadataV2(meta, { referenceText = null } = {}) {
  const errors = []
  if (meta == null) return { valid: true, errors, normalized: null } // absence is valid → free
  if (typeof meta !== 'object') return { valid: false, errors: ['INVALID_SEMANTIC_ASSESSMENT_STRATEGY'], normalized: null }
  const strategy = meta.strategy
  if (!SEMANTIC_STRATEGIES.includes(strategy)) {
    return { valid: false, errors: ['INVALID_SEMANTIC_ASSESSMENT_STRATEGY'], normalized: null }
  }
  if (strategy === 'equivalent_meaning') {
    if (!referenceText || typeof referenceText !== 'string' || !referenceText.trim()) errors.push('EQUIVALENT_TARGET_WITHOUT_TEXT')
    const essential = Array.isArray(meta.essential_words) ? meta.essential_words.filter((w) => typeof w === 'string' && w.trim()) : []
    if (essential.length === 0) errors.push('EQUIVALENT_TARGET_WITHOUT_ESSENTIAL_WORDS')
    // Each essential word must appear (token boundary, case-insensitive) in the
    // authored reference — essential words are of the reference, not invented.
    if (referenceText) {
      for (const w of essential) {
        if (!wordInText(referenceText, w)) errors.push('SEMANTIC_TARGET_REFERENCES_NON_AUTHORED_TEXT')
      }
    }
  } else if (strategy === 'guided_intent') {
    if (!meta.requested_intent || !KNOWN_SEMANTIC_INTENTS.includes(meta.requested_intent)) errors.push('INVALID_REQUESTED_INTENT')
  }
  return { valid: errors.length === 0, errors, normalized: errors.length === 0 ? meta : null }
}

function wordInText(text, word) {
  const re = new RegExp(`(?:^|[^a-z0-9])${String(word).toLowerCase().replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}(?:$|[^a-z0-9])`, 'i')
  return re.test(String(text))
}

/**
 * Build the SemanticAssessmentRequestV2 for a plan + learner text. PURE.
 *   buildSemanticAssessmentRequestV2({ plan, text, responseType })
 * Falls back to a `free` request whenever metadata is absent or invalid, or the
 * recipe is not a production recipe.
 */
export function buildSemanticAssessmentRequestV2({ plan, text, responseType = 'text' }) {
  const context = { situation: plan?.context ?? null, text_pt: plan?.text_pt ?? null }
  const provenanceBase = {
    target_id: plan?.primary_target?.target_id ?? null,
    exemplar_id: plan?.exemplar_id ?? null,
    construction_id: plan?.construction_id ?? null,
  }
  const free = (reason, source = 'fallback') => ({
    bridge_version: SEMANTIC_ASSESSMENT_BRIDGE_VERSION,
    strategy: 'free',
    assessment_mode: 'free',
    text: text ?? null,
    reference_text: plan?.text_en ?? null,
    response_type: responseType,
    requested_intent: null,
    equivalent_target: null,
    context,
    provenance: { source, reason, ...provenanceBase },
    fallback_reason: reason,
  })

  if (!plan || !PRODUCTION_RECIPES.has(plan.recipe)) return free('NON_PRODUCTION_RECIPE')

  const meta = plan.semantic_assessment || null
  const source = plan.semantic_assessment_source || 'plan'
  if (!meta || meta.strategy === 'free' || !meta.strategy) return free('NO_AUTHORED_SEMANTIC_TARGET')

  const v = validateSemanticAssessmentMetadataV2(meta, { referenceText: plan.text_en })
  if (!v.valid) return free('INVALID_SEMANTIC_METADATA')

  if (meta.strategy === 'equivalent_meaning') {
    const essential = meta.essential_words.filter((w) => typeof w === 'string' && w.trim())
    return {
      bridge_version: SEMANTIC_ASSESSMENT_BRIDGE_VERSION,
      strategy: 'equivalent_meaning',
      assessment_mode: 'equivalent',
      text: text ?? null,
      reference_text: plan.text_en,
      response_type: responseType,
      requested_intent: null,
      equivalent_target: { text: plan.text_en, essential_words: [...essential] },
      context,
      provenance: { source, reason: 'AUTHORED_EQUIVALENT_TARGET', ...provenanceBase },
      fallback_reason: null,
    }
  }

  if (meta.strategy === 'guided_intent') {
    return {
      bridge_version: SEMANTIC_ASSESSMENT_BRIDGE_VERSION,
      strategy: 'guided_intent',
      assessment_mode: 'guided',
      text: text ?? null,
      reference_text: plan.text_en,
      response_type: responseType,
      requested_intent: meta.requested_intent,
      equivalent_target: null,
      context,
      provenance: { source, reason: 'AUTHORED_GUIDED_INTENT', ...provenanceBase },
      fallback_reason: null,
    }
  }

  return free('INVALID_SEMANTIC_METADATA')
}

// Only the fields the engine actually consumes are forwarded from a request.
export function requestToAnalyzeParamsV2(request) {
  return {
    text: request.text,
    assessmentMode: request.assessment_mode,
    requestedIntent: request.requested_intent ?? null,
    equivalentTarget: request.equivalent_target ?? null,
  }
}

export { strategyToMode }
